#append operation

animal = ["cat","dog"]
animal.append("wolf")
print(animal)

#extend operation

lang1 =["english","hindi"]
lang2=["spanish","french"]
lang1.extend(lang2)
print(lang1)

#insert operation

alpha= ["a","b","d","e"]
alpha.insert(2,"c")
print(alpha)

#remove operation

animal = ["cat","dog","penguin","wolf"]
animal.remove("penguin")
print(animal)

